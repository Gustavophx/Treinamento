# Conversão de tipos
idade = input('Qual é a sua idade ? ')
print(int(idade) > 18) # Para converter alguma informação digite pra qual deseja e colocar entre ()
# str() = letras ou string, int() = numeros inteiros e float()= numeros com , ou . 

# Receber valores decimais
altura_da_parede = input('Qual é a altura da parede ? ')
print(float(altura_da_parede) > 2.10)