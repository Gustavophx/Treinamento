# Ordenando listas simples

valores = [31, 23, 6, 36, 21, 33, 37, 7, 20, 23]
# Ordenador em ordem crescente
valores.sort() # apenas modifica a lista originial e não retorna uma lista nova, .sort irar organizar em ordem crescente.
# Ordenar em ordem decrescente
print(valores)
valores.sort(reverse=True) # com a reverse ira ser ordem decrescente 
print(valores)
# Inevrter uma lista
valores.reverse() # A lista começa de tras pra frente
print(valores)
